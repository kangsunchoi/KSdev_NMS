const I18nContext = createContext({ t: (k) => k, lang: "ko", setLang: () => {} });

export const I18nProvider = ({ children }) => {
  const [lang, setLangState] = useState(() => localStorage.getItem("nv_lang") || "ko");
  const setLang = useCallback((l) => {
    localStorage.setItem("nv_lang", l);
    setLangState(l);
  }, []);
  const t = useCallback(
    (key) => (DICT[lang] && DICT[lang][key]) || DICT.en[key] || key,
    [lang]
  );
  return <I18nContext.Provider value={{ t, lang, setLang }}>{children}</I18nContext.Provider>;
};

export const useI18n = () => useContext(I18nContext);

export default useI18n;
